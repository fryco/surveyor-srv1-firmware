/* estimates the robots pose */
void svs_visual_odometry(
    int right_turn_pixels,
    int tollerance)
{
#ifdef SVS_PROFILE
    int t = readRTC();
#endif

    int i, j, best_dx, x, expected_x, x_prev, y, no_of_feats, no_of_feats_prev, n, f, f_prev;
    int dx, dx2, min_dx, state, av_dx=0, hits=0;
    n = 0;
    f = 0;    
    f_prev = 0;
    for (y = 4; y < (int)imgHeight - 4; y += SVS_VERTICAL_SAMPLING, n++)
    {        
        no_of_feats = svs_data->features_per_row[n];
        no_of_feats_prev = svs_data_previous->features_per_row[n];
        // current edge features         
        for (i = 0; i < no_of_feats; i++) {
            x = svs_data_previous->feature_x[f + i];
            min_dx = 9999;
            best_dx = 9999;
            state = 0;
            expected_x = x - right_turn_pixels;
            // previous edge features 
            for (j = 0; j < no_of_feats_prev; j++) {
                x_prev = svs_data_previous->feature_x[f_prev + j];
                dx = x_prev - expected_x;
                if ((dx > -tollerance) && (dx < tollerance)) {
                    // within tollerance
                    state = 1;
                    if (dx >= 0)
                        dx2 = dx;
                    else
                        dx2 = -dx;
                    if (dx2 < min_dx) {
                        min_dx = dx2;
                        best_dx = x - x_prev;
                        if (min_dx < 4) break;
                    }
                }
                else {
                    // outside of tollerance 
                    if (state == 1) break;
                }
            }
            if (best_dx != 9999) {
                av_dx += best_dx;
                hits++;
            }
            
        }        
        f += no_of_feats;
        f_prev += no_of_feats_prev;
    }
    
    if (hits > 0) {
        av_dx /= hits;
    }
    else {
        av_dx = right_turn_pixels;
    }
    
    // update orientation of the robot 
    robot_orientation_degrees += av_dx * SVS_FOV_DEGREES / ((int)imgWidth*2);
    if (robot_orientation_degrees < 0) robot_orientation_degrees += 360;
    if (robot_orientation_degrees >= 360) robot_orientation_degrees -= 360;
    
#ifdef SVS_PROFILE
    printf("svs_visual_odometry %d mS\r\n", readRTC() - t);
#endif
}

/* re-centers the grid map */
void svs_map_recenter()
{
#ifdef SVS_PROFILE
    int t;
#endif

    /* compute change in translation */
    int dx = robot_x_mm - map_x_mm;
    int dy = robot_y_mm - map_y_mm;
    
    if ((dx < -SVS_MAP_CELL_SIZE_MM) || (dx > SVS_MAP_CELL_SIZE_MM) ||
        (dy < -SVS_MAP_CELL_SIZE_MM) || (dy > SVS_MAP_CELL_SIZE_MM)) {

#ifdef SVS_PROFILE
        t = readRTC();
#endif

        int n, i, offset, cell_x_mm, cell_y_mm, cell_x, cell_y, rP, tP;
        unsigned char* buffer_occupancy = (unsigned char*)FRAME_BUF3;
        int* buffer_coords = (int*)FRAME_BUF4;
        const int offset2 = -SVS_MAP_TDIM*1/4;

        /* clear the buffer */
        const int array_length = (SVS_MAP_RDIM + SVS_MAP_HYPRDIM)*SVS_MAP_TDIM*2;
        for (i = array_length-2; i >= 0; i-=2) buffer_coords[i] = SVS_EMPTY_CELL;
        memset((void*)buffer_occupancy, '\0', array_length);

        /* update map cells */
        offset = SVS_MAP_WIDTH_CELLS/2;    
        for (i = array_length-2; i >= 0; i -= 2) {
            if (Hpolar_map_coords[i] != SVS_EMPTY_CELL) {
            
                /* update cell position */
                cell_x_mm = Hpolar_map_coords[i] - map_x_mm - dx;
                cell_y_mm = Hpolar_map_coords[i+1] - map_y_mm - dy;
            
                /* convert millimetres to a cartesian grid coordinate */
                cell_x = offset + (cell_x_mm / SVS_MAP_CELL_SIZE_MM);
                cell_y = offset + (cell_y_mm / SVS_MAP_CELL_SIZE_MM);
                if ((cell_x >= 0) && (cell_x < SVS_MAP_WIDTH_CELLS) &&
                    (cell_y >= 0) && (cell_y < SVS_MAP_WIDTH_CELLS)) {  
                                   
                    /* convert from cartesian grid to H-polar grid using lookup */
                    n = (cell_y*SVS_MAP_WIDTH_CELLS + cell_x)*2;
                    rP = HpolarLookup[n];
                    tP = HpolarLookup[n+1] + offset2;
                    if (tP < 0) tP += SVS_MAP_TDIM;
                    if (tP >= SVS_MAP_TDIM) tP -= SVS_MAP_TDIM;
                    n = (rP*SVS_MAP_TDIM + tP)*2;
                    buffer_occupancy[n] = Hpolar_map_occupancy[i];
                    buffer_occupancy[n+1] = Hpolar_map_occupancy[i+1];
                    buffer_coords[n] = cell_x_mm + map_x_mm;
                    buffer_coords[n+1] = cell_y_mm + map_y_mm;
                }
            }
        }
    
        /* copy buffer back to the map */
        memcpy((void*)Hpolar_map_coords, (void*)buffer_coords, array_length*sizeof(int));
        memcpy((void*)Hpolar_map_occupancy, (void*)buffer_occupancy, array_length);
    
        /* set the new map centre position */
        map_x_mm = robot_x_mm;
        map_y_mm = robot_y_mm;
        
        prev_dist_moved_mm = dist_moved_mm;
        
#ifdef SVS_PROFILE
        printf("svs_map_recenter %d mS\r\n", readRTC() - t);
#endif
    }
}

/* updates a local H-polar grid map */
void svs_map_update(
int no_of_matches,
int max_disparity_percent)
{
#ifdef SVS_PROFILE
    int t = readRTC();
#endif

    int i, j, d, w, rot, x, y, disp, disp_radius;
    int x_mm, y_mm, curr_y_mm, x_rotated_mm, y_rotated_mm, disp_mmx100;
    int rP, tP, r, n, SinVal, CosVal;
    int cell_x, cell_y;
    int ground_y = (int)imgHeight * svs_ground_y_percent/100;
    int ground_y_sloped=0, ground_height_sloped=0;    
    int half_width = (int)imgWidth/2;
    int max = (int)imgWidth / (int)SVS_HORIZONTAL_SAMPLING;
    const int range_const = SVS_FOCAL_LENGTH_MMx100 * SVS_BASELINE_MM;
    //int focal_const = SVS_FOCAL_LENGTH_MMx100 * (int)imgWidth;
    int max_disp_pixels = max_disparity_percent * (int)imgWidth / 100;    
    const int max_r = SVS_MAP_RDIM + SVS_MAP_HYPRDIM;
    const int polar_stride = SVS_MAP_TDIM*2;
    const int offset2 = -SVS_MAP_TDIM*1/4;
    int dx = robot_x_mm - map_x_mm;
    int dy = robot_y_mm - map_y_mm;
    const int offset = SVS_MAP_WIDTH_CELLS/2;

    int forward_movement_mm = 0;
    int forward_movement_hits = 0;
        
    /* fill in any missing disparity values using the ground plane
       and estimate the forward movement of the robot */    
    for (i = 0; i < max; i++) {
        x = i * SVS_HORIZONTAL_SAMPLING;
        y = footline[i];
        ground_y_sloped = ground_y + ((half_width - x) * svs_ground_slope_percent / 100);
        ground_height_sloped = (int)imgHeight - 1 - ground_y_sloped;
        disp = (y - ground_y_sloped) * max_disp_pixels / ground_height_sloped;
        disp_mmx100 = disp * svs_sensor_width_mmx100 / (int)imgWidth;
        if (disp_mmx100 > 0) {
            // get position of the feature in space 
            y_mm = range_const / disp_mmx100;

            if (y_mm < 1500) {
                if (footline_dist_mm[i] != 0) {
                    forward_movement_mm += y_mm - footline_dist_mm[i];
                    forward_movement_hits++;
                }
                footline_dist_mm[i] = y_mm;
            }
            else {
                footline_dist_mm[i] = 0;
            }
        }
        else {
            footline_dist_mm[i] = 0;
        }
    }    
    if (forward_movement_hits > 0) {
        forward_movement_mm /= forward_movement_hits;
        //printf("forward movement %d mm\r\n", forward_movement_mm);
    }
        
    /* estimate the change in orientation of the robot */
    if (svs_right_turn_percent != 0) {
        svs_visual_odometry(
            svs_right_turn_percent * (int)imgWidth / 100, 
            svs_turn_tollerance_percent * (int)imgWidth / 100);    
        svs_right_turn_percent = 0;
        //printf("Orient %d\r\n", robot_orientation_degrees);
    }
    
    /* update the position/pose estimate */
    if (robot_moving != 0) {
        int pwm = (lspeed + rspeed)/2;
        if (pwm < 0) pwm = -pwm;
        const int reference_variance0 = 7;
        const int reference_speed0 = 95;
        const int reference_pwm0 = 19;
        const int reference_variance1 = 22;
        const int reference_speed1 = 265;
        const int reference_pwm1 = 31;
        int robot_speed_mmsec = 
            reference_speed0 + 
            ((pwm - reference_pwm0) * 
             (reference_speed1 - reference_speed0) / 
             (reference_pwm1 - reference_pwm0));
        int speed_variance_mmsec = 
            reference_variance0 + 
            ((pwm - reference_pwm0) * 
             (reference_variance1 - reference_variance0) / 
             (reference_pwm1 - reference_pwm0));
        int time_elapsed_mS = readRTC() - move_start_time;
        int dist_mm_min = time_elapsed_mS * (robot_speed_mmsec - speed_variance_mmsec) / 1000;
        int dist_mm_max = time_elapsed_mS * (robot_speed_mmsec + speed_variance_mmsec) / 1000;
        if (lspeed + rspeed < 0) {
            dist_mm_min = -dist_mm_min;
            dist_mm_max = -dist_mm_max;
        }
        
        dist_moved_mm = ((dist_mm_min + dist_mm_max)/2) - prev_dist_moved_mm;
        
        robot_x_mm = map_x_mm + (dist_moved_mm*SinLookup[robot_orientation_degrees]/10000);
        CosVal = 90 - robot_orientation_degrees;
        if (CosVal < 0) CosVal += 360;
        robot_y_mm = map_x_mm + (dist_moved_mm*SinLookup[CosVal]/10000);
    }
    else {
        prev_dist_moved_mm = 0;
    }
    
    /* re-center the map if the robot has moved a sufficient distance */
    svs_map_recenter();
        
    //int max_range = (SVS_MAP_WIDTH_CELLS-4) * SVS_MAP_CELL_SIZE_MM / 2;
    int on_ground_plane;
    for (i = 0; i < no_of_matches*4; i += 4) {           
        if (svs_matches[i] > 0) {  // match probability > 0
            x = svs_matches[i + 1];
            y = svs_matches[i + 2];
            if (y <= footline[x / SVS_HORIZONTAL_SAMPLING])
                on_ground_plane = 0;
            else
                on_ground_plane = 1;
            
            disp = svs_matches[i + 3];
            disp_mmx100 = disp * svs_sensor_width_mmx100 / (int)imgWidth;
            if (disp_mmx100 > 0) {
                // get position of the feature in space
                y_mm = range_const / disp_mmx100;
                
                //printf("disp_mmx100 %d\r\nsvs_sensor_width_mmx100 %d\r\n", disp_mmx100, svs_sensor_width_mmx100);
                //printf("y %d / %d = %d mm\r\n", range_const, disp_mmx100, y_mm);
                
                //if (y_mm > max_range) y = max_range; // hack!

                disp_radius = disp/2;
                if (disp_radius > 10) disp_radius = 10;
                curr_y_mm = y_mm;                    
                int rot_off;
                int max_rot_off = (int)SVS_FOV_DEGREES/2;
                                
                rot_off = (x - half_width) * (int)SVS_FOV_DEGREES / (int)imgWidth;
                if ((rot_off > -max_rot_off) && (rot_off < max_rot_off)) {
                    rot = robot_orientation_degrees + rot_off; 

                    CosVal = 90 - rot;
                    if (CosVal < 0) CosVal += 360;
                    CosVal = SinLookup[CosVal];
                    SinVal = SinLookup[rot];
                    
                    // rotate by the orientation of the robot 
                    y_rotated_mm = SinVal * curr_y_mm / (int)10000;
                    x_rotated_mm = CosVal * curr_y_mm / (int)10000;                        
            
                    // convert millimetres to a cartesian grid coordinate 
                    cell_x = offset + ((x_rotated_mm + dx) / (int)SVS_MAP_CELL_SIZE_MM);
                    cell_y = offset + ((y_rotated_mm + dy) / (int)SVS_MAP_CELL_SIZE_MM);
                    if ((cell_x >= 0) && (cell_x < SVS_MAP_WIDTH_CELLS) &&
                        (cell_y >= 0) && (cell_y < SVS_MAP_WIDTH_CELLS)) { 
                        
                        w = disp * 40 / max_disp_pixels;
                            
                        // absolute position of the point in cartesian space 
                        x_mm = x_rotated_mm + robot_x_mm;
                        y_mm = y_rotated_mm + robot_y_mm;
                                
                        // convert from cartesian grid to H-polar grid using lookup 
                        n = (cell_y*SVS_MAP_WIDTH_CELLS + cell_x)*2;
                        rP = HpolarLookup[n];
                        d = HpolarLookup[n+1] + offset2;
                        
                        for (j = d-w; j <= d+w; j++) {
                            tP = j;
                            if (tP >= SVS_MAP_TDIM) tP -= SVS_MAP_TDIM;
                            if (tP < 0) tP += SVS_MAP_TDIM;
                
                            // vacancy 
                            n = tP*2;
                            int prob = 5;
                            if (prob > rP) prob = rP;
                            for (r = 0; r < rP-1; r++, n += polar_stride) {
                                if (Hpolar_map_coords[n] == SVS_EMPTY_CELL) {
                                    Hpolar_map_coords[n] = x_mm;
                                    Hpolar_map_coords[n+1] = y_mm;
                                }
                                else {
                                    Hpolar_map_coords[n] += (x_mm - Hpolar_map_coords[n])/2;
                                    Hpolar_map_coords[n+1] += (y_mm - Hpolar_map_coords[n+1])/2;
                                }
                                if (Hpolar_map_occupancy[n] < 245) {
                                    // increment vacancy                                     
                                    Hpolar_map_occupancy[n] += prob;
                                    prob--;
                                    if (prob < 1) prob = 1;
                                }
                                else {
                                    // decrement occupancy 
                                    if (Hpolar_map_occupancy[n+1] > 0) Hpolar_map_occupancy[n+1]--;
                                }                    
                            }
                            if (on_ground_plane == 0) {
                                // occupancy
                                n++;
                                prob = 10;
                                while (r < rP+2+w) {
                                    if (Hpolar_map_coords[n-1] == SVS_EMPTY_CELL) {
                                        Hpolar_map_coords[n-1] = x_mm;
                                        Hpolar_map_coords[n] = y_mm;
                                    }
                                    else {
                                        Hpolar_map_coords[n-1] += (x_mm - Hpolar_map_coords[n-1])/2;
                                        Hpolar_map_coords[n] += (y_mm - Hpolar_map_coords[n])/2;
                                    }
                                    if (r < max_r) {
                                        if (Hpolar_map_occupancy[n] < 245) {
                                            // increment occupancy (very crude sensor model)
                                            Hpolar_map_occupancy[n] += prob;
                                            prob--;
                                            if (prob < 1) prob = 1;
                                        }
                                        else {
                                            // decrement vacancy 
                                            if (Hpolar_map_occupancy[n-1] > 0) 
                                                Hpolar_map_occupancy[n-1]--;
                                        }
                                    }
                                    else {
                                        break;
                                    }
                                    n += polar_stride;
                                    r++;
                                }
                            }
                        }
                    }
                }
            }            
        }
    }
        
#ifdef SVS_PROFILE
    printf("svs_map_update %d mS\r\n", readRTC() - t);
#endif    
}

/* finds the ground plane */
void svs_ground_plane()
{
#ifdef SVS_PROFILE
    int t = readRTC();
#endif

    if (svs_enable_ground_priors) {
        int i, j, x, y, diff, prev_y = 0, prev_i=0;
        
        /* Remove points which have a large vertical difference.
           Successive points with small vertical difference are likely
           to correspond to real borders rather than carpet patterns, etc*/
        int max_diff = (int)imgHeight / 30;        
        for (i = 0; i < (int)imgWidth / SVS_HORIZONTAL_SAMPLING; i++) {
            x = i * SVS_HORIZONTAL_SAMPLING;
            y = footline[i];
            if (y != 0) {
                if (prev_y != 0) {
                    diff = y - prev_y;
                    if (diff < 0) diff = -diff;
                    if (diff > max_diff) {
                        if (y < prev_y) {
                            footline[prev_i] = 0;
                        }
                        else {
                            footline[i] = 0;
                        }
                    }
                }
                prev_i = i;
                prev_y = y;
            }
        }
        
        /* fill in missing data to create a complete ground plane */
        prev_i = 0;
        prev_y = (int)imgHeight;
        int max = (int)imgWidth / SVS_HORIZONTAL_SAMPLING;
        for (i = 0; i < max; i++) {
            x = i * SVS_HORIZONTAL_SAMPLING;
            y = footline[i];
            if (y != 0) {
                if (prev_y == 0) prev_y = y;
                for (j = prev_i; j < i; j++) {
                    footline[j] = prev_y + ((j - prev_i) * (y - prev_y) / (i - prev_i));
                }
                prev_y = y;
                prev_i = i;
            }
        }
        for (j = prev_i; j < max; j++) {
            footline[j] = prev_y;
        }
    }
#ifdef SVS_PROFILE
    printf("svs_ground_plane %d mS\r\n", readRTC() - t);
#endif    
}

